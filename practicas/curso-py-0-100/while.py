# while es un bucle que permite realizar multiples iteraciones basándose en el resultado de una expresión lógica o
# condición que puede tener como resultado un valor True o False

indice = 1
while indice < 10:
    print("Valor actual: {}".format(indice))
    indice += 1
print("Se ha cerrado el bucle while")

inicio = 2
while inicio <= 100:
    print("Número par: {}".format(inicio))
    inicio += 2
print("Se ha terminado el segundo while.")
